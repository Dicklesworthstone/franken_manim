/// `sqrt` — delegated to the hardware instruction on purpose: IEEE 754
/// requires correct rounding, so it is bit-identical on every certified
/// target already. Max error: 0.5 ulp (correctly rounded).
#[must_use]
pub fn sqrt(x: f64) -> f64 {
    x.sqrt()
}

/// The standard/certified seam (§6.6): one function table, chosen at
/// engine construction. `certified` construction takes [`CERTIFIED`];
/// `standard` may take [`FAST`]. Call sites hold the table, never a cfg.
#[derive(Clone, Copy)]
pub struct Transcendentals {
    pub sin: fn(f64) -> f64,
    pub cos: fn(f64) -> f64,
    pub tan: fn(f64) -> f64,
    pub asin: fn(f64) -> f64,
    pub acos: fn(f64) -> f64,
    pub atan: fn(f64) -> f64,
    pub atan2: fn(f64, f64) -> f64,
    pub exp: fn(f64) -> f64,
    pub ln: fn(f64) -> f64,
    pub log2: fn(f64) -> f64,
    pub pow: fn(f64, f64) -> f64,
    pub sqrt: fn(f64) -> f64,
    pub cbrt: fn(f64) -> f64,
    pub sinh: fn(f64) -> f64,
    pub cosh: fn(f64) -> f64,
    pub tanh: fn(f64) -> f64,
}

/// The certified table: fmn-dmath exclusively. This is the definition the
/// certified engine's bits are a function of.
pub const CERTIFIED: Transcendentals = Transcendentals {
    sin,
    cos,
    tan,
    asin,
    acos,
    atan,
    atan2,
    exp,
    ln,
    log2,
    pow,
    sqrt,
    cbrt,
    sinh,
    cosh,
    tanh,
};

/// The fast table: platform libm via `std`. Deterministic per
/// build/platform only — `standard` mode, always labeled, never certified.
pub const FAST: Transcendentals = Transcendentals {
    sin: f64::sin,
    cos: f64::cos,
    tan: f64::tan,
    asin: f64::asin,
    acos: f64::acos,
    atan: f64::atan,
    atan2: f64::atan2,
    exp: f64::exp,
    ln: f64::ln,
    log2: f64::log2,
    pow: f64::powf,
    sqrt,
    cbrt: f64::cbrt,
    sinh: f64::sinh,
    cosh: f64::cosh,
    tanh: f64::tanh,
};

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn seam_tables_are_swappable_at_construction() {
        // An "engine" holding its table — the seam pattern call sites use.
        struct Engine {
            math: Transcendentals,
        }
        let certified = Engine { math: CERTIFIED };
        let fast = Engine { math: FAST };
        // Both compute; the certified one is the pinned definition.
        let x = 1.234_567_89_f64;
        let c = (certified.math.sin)(x);
        let f = (fast.math.sin)(x);
        assert!((c - f).abs() < 1e-15);
        assert_eq!(c.to_bits(), sin(x).to_bits());
    }

    #[test]
    fn sqrt_is_correctly_rounded_delegation() {
        assert_eq!(sqrt(2.0).to_bits(), 2.0_f64.sqrt().to_bits());
        assert!(sqrt(-1.0).is_nan());
        assert_eq!(sqrt(0.0).to_bits(), 0.0_f64.to_bits());
    }
}
