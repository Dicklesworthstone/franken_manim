//! Owned deterministic elementary functions: bit-reproducible transcendentals for the certified path (§6.6, D-17).
//!
//! `std`'s transcendentals defer to platform libm and differ across
//! glibc/macOS/WASM; this engine is transcendental-dense (arc construction,
//! per-vertex atan2 joints, rate functions, glow falloffs, tanh
//! normalization, Euler conversions). fmn-dmath is the owned,
//! fixed-implementation layer: classical FDLIBM-style range reduction and
//! fixed-coefficient, fixed-order minimax polynomials, bit-identical on
//! every certified target, locked by the committed vector files under
//! `vectors/` (generated by `scripts/gen_dmath_vectors.py` from mpmath
//! ground truth; regenerate, never hand-edit).
//!
//! **Accuracy bounds** are documented per function and measured against
//! correctly-rounded ground truth by `tests/vectors.rs`. `sqrt` is the one
//! deliberate delegation: IEEE 754 requires correct rounding, so
//! `f64::sqrt`/`f32::sqrt` are already bit-identical everywhere.
//!
//! **The mode seam** (§6.6): `standard` mode may use fast paths;
//! `certified` uses fmn-dmath exclusively. The seam is the
//! [`Transcendentals`] function table, chosen once at engine construction
//! — never a scattered `cfg`.
//!
//! **SIMD posture** (§17.3): every kernel is a fixed-coefficient,
//! fixed-order polynomial over range reduction, so later `std::simd`
//! vectorization can reproduce the scalar path bit-for-bit lane by lane.
//! Nothing here is vectorized yet; nothing here precludes it.
//!
//! **f32 entry points** compute through the f64 implementations and round
//! once at the end ([`f32api`]). That is deterministic (the f64 layer is),
//! at least as accurate as a native-f32 polynomial, and half the surface
//! to audit. The double-rounding caveat is documented on the module.
#![forbid(unsafe_code)]

